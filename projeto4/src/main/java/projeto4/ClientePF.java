package projeto4;

public class ClientePF extends Cliente {
	
	private String numeroCPF;
	private String nomeGestor;
	private String telefoneGestor;
	/**
	 * @return the numeroCPF
	 */
	public String getNumeroCPF() {
		return numeroCPF;
	}
	/**
	 * @param numeroCPF the numeroCPF to set
	 */
	public void setNumeroCPF(String numeroCPF) {
		this.numeroCPF = numeroCPF;
	}
	/**
	 * @return the nomeGestor
	 */
	public String getNomeGestor() {
		return nomeGestor;
	}
	/**
	 * @param nomeGestor the nomeGestor to set
	 */
	public void setNomeGestor(String nomeGestor) {
		this.nomeGestor = nomeGestor;
	}
	/**
	 * @return the telefoneGestor
	 */
	public String getTelefoneGestor() {
		return telefoneGestor;
	}
	/**
	 * @param telefoneGestor the telefoneGestor to set
	 */
	public void setTelefoneGestor(String telefoneGestor) {
		this.telefoneGestor = telefoneGestor;
	}

}
